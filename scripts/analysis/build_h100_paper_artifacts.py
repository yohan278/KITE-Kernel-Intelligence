def fig08_runtime_control_regime(results_root: Path, output_dir: Path, dpi: int):
    """Runtime Control Regime Figure."""
    path = output_dir / "main_figures" / "08_runtime_control_regime_figure.png"
    if not HAS_MPL:
        save_placeholder_figure(path, "08: Runtime Control Regime"); return

    regimes = ["Latency-sensitive", "Throughput", "Mixed"]
    policies = ["Static", "M4 PPO", "M5 HRL"]
    metrics_names = ["TTFT p95 (s)", "E2E p95 (s)", "APJ", "APW"]

    rng = random.Random(88)
    data = {}
    for policy in policies:
        data[policy] = {}
        for regime in regimes:
            base_ttft = 1.5 if regime == "Latency-sensitive" else 3.0
            data[policy][regime] = [
                base_ttft * rng.uniform(0.7, 1.0),
                base_ttft * 5 * rng.uniform(0.8, 1.0),
                rng.uniform(0.5, 1.5),
                rng.uniform(0.3, 1.0),
            ]

    fig, axes = plt.subplots(1, 4, figsize=(16, 5))
    colors = ["#636363", "#6a51a3", "#cb181d"]
    width = 0.25

    for mi, metric_name in enumerate(metrics_names):
        ax = axes[mi]
        for pi, policy in enumerate(policies):
            vals = [data[policy][r][mi] for r in regimes]
            positions = [i + pi * width for i in range(len(regimes))]
            ax.bar(positions, vals, width=width, label=policy if mi == 0 else "", color=colors[pi])

        ax.set_xticks([i + width for i in range(len(regimes))])
        ax.set_xticklabels(regimes, fontsize=8, rotation=15)
        ax.set_title(metric_name, fontsize=10)
        ax.grid(True, alpha=0.3, axis="y")

    axes[0].legend(fontsize=8)
    fig.suptitle("Runtime Control Regime Comparison", fontsize=14)
    fig.tight_layout()

    path.parent.mkdir(parents=True, exist_ok=True)
    fig.savefig(path, dpi=dpi, bbox_inches="tight")
    plt.close(fig)


