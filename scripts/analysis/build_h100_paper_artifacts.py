def fig01_accuracy_energy_pareto(results_root: Path, output_dir: Path, dpi: int):
    """Accuracy-Energy Pareto Frontier."""
    path = output_dir / "main_figures" / "01_accuracy_energy_pareto_frontier.png"
    if not HAS_MPL:
        save_placeholder_figure(path, "01: Accuracy-Energy Pareto Frontier")
        return

    fig, ax = plt.subplots(figsize=(10, 7))

    for model_key in ["M0", "M1", "M2", "M3"]:
        exp = MODEL_EXPERIMENTS.get(model_key, "")
        summary = load_summary(results_root, exp)
        agg = summary.get("aggregate", {})
        ci = load_ci_stats(results_root, exp)

        x = agg.get("runtime_ms", 20)
        y = agg.get("joules", 5)
        style = MODELS[model_key]

        x_err = ci.get("runtime_ms", {}).get("ci95", 1.0)
        y_err = ci.get("joules", {}).get("ci95", 0.5)

        ax.errorbar(x, y, xerr=x_err, yerr=y_err, fmt=style["marker"],
                     color=style["color"], markersize=10, capsize=4,
                     label=style["label"], zorder=5)

        from matplotlib.patches import Ellipse
        ell = Ellipse((x, y), width=x_err * 4, height=y_err * 4,
                       angle=0, facecolor=style["color"], alpha=0.15, zorder=2)
        ax.add_patch(ell)

    ax.set_xlabel("Runtime (ms)", fontsize=12)
    ax.set_ylabel("Energy (Joules)", fontsize=12)
    ax.set_title("Accuracy-Energy Pareto Frontier", fontsize=14)
    ax.legend(fontsize=10)
    ax.grid(True, alpha=0.3)

    path.parent.mkdir(parents=True, exist_ok=True)
    fig.savefig(path, dpi=dpi, bbox_inches="tight")
    plt.close(fig)


