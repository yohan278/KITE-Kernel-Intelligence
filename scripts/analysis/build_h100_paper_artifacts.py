def parse_args():
    p = argparse.ArgumentParser(description="Build paper artifacts from H100 results")
    p.add_argument("--results-root", type=Path, default=PROJECT_ROOT / "results" / "h100" / "2026-03")
    p.add_argument("--output", type=Path, default=None)
    p.add_argument("--dpi", type=int, default=150)
    return p.parse_args()


def load_summary(results_root: Path, exp_name: str) -> dict:
    path = results_root / exp_name / f"{exp_name}_summary.json"
    if path.exists():
        with open(path) as f:
            return json.load(f)
    return {}


def load_metrics_csv(results_root: Path, exp_name: str) -> list[dict]:
    path = results_root / exp_name / f"{exp_name}_metrics.csv"
    if not path.exists():
        return []
    with open(path) as f:
        return list(csv.DictReader(f))


def load_ci_stats(results_root: Path, exp_name: str) -> dict:
    path = results_root / exp_name / f"{exp_name}_ci_stats.json"
    if path.exists():
        with open(path) as f:
            return json.load(f)
    return {}


def write_csv_table(path: Path, rows: list[dict]):
    if not rows:
        return
    path.parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w", newline="") as f:
        w = csv.DictWriter(f, fieldnames=list(rows[0].keys()))
        w.writeheader()
        w.writerows(rows)


def write_md_table(path: Path, rows: list[dict]):
    if not rows:
        return
    path.parent.mkdir(parents=True, exist_ok=True)
    headers = list(rows[0].keys())
    lines = ["| " + " | ".join(headers) + " |"]
    lines.append("| " + " | ".join("---" for _ in headers) + " |")
    for r in rows:
        vals = [str(r.get(h, "")) for h in headers]
        lines.append("| " + " | ".join(vals) + " |")
    path.write_text("\n".join(lines) + "\n")


def save_placeholder_figure(path: Path, title: str):
    path.parent.mkdir(parents=True, exist_ok=True)
    if HAS_MPL:
        fig, ax = plt.subplots(figsize=(8, 6))
        ax.text(0.5, 0.5, title, ha="center", va="center", fontsize=14, transform=ax.transAxes)
        ax.set_xlim(0, 1)
        ax.set_ylim(0, 1)
        ax.axis("off")
        fig.savefig(path, dpi=100, bbox_inches="tight")
        plt.close(fig)
    else:
        path.write_text(f"PLACEHOLDER: {title}\n")


